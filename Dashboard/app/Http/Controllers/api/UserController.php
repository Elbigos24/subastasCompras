     * Remove the specified resource from storage.
     */
    public function destroy(string $id)
    {
        $user = Usser::find($id);
        if (!$user) {
            return response()->json(['message' => 'User not found'], 404);
        }
        $user->delete();
        return response()->json(['message' => 'User deleted successfully']);

